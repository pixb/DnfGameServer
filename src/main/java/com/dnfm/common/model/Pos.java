package com.dnfm.common.model;

public class Pos {
   private int x = 474;
   private int y = 234;
   private int town = 1;
   private int area = 3;

   public int getX() {
      return this.x;
   }

   public void setX(int x) {
      this.x = x;
   }

   public int getY() {
      return this.y;
   }

   public void setY(int y) {
      this.y = y;
   }

   public int getTown() {
      return this.town;
   }

   public void setTown(int town) {
      this.town = town;
   }

   public int getArea() {
      return this.area;
   }

   public void setArea(int area) {
      this.area = area;
   }
}
