package com.dnfm.game.role;

public class ActivityStatus {
   private String name;
   private int start;
   private int end;

   public String getName() {
      return this.name;
   }

   public void setName(String name) {
      this.name = name;
   }

   public int getStart() {
      return this.start;
   }

   public void setStart(int start) {
      this.start = start;
   }

   public int getEnd() {
      return this.end;
   }

   public void setEnd(int end) {
      this.end = end;
   }
}
