package com.dnfm.game.enter;

public class Position {
   private short x;
   private short y;

   public short getX() {
      return this.x;
   }

   public void setX(short x) {
      this.x = x;
   }

   public short getY() {
      return this.y;
   }

   public void setY(short y) {
      this.y = y;
   }
}
