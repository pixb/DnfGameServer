package com.dnfm.game.friend.model;

public class FriendMemo {
   private String gid;
   private String memo;

   public String getGid() {
      return this.gid;
   }

   public void setGid(String gid) {
      this.gid = gid;
   }

   public String getMemo() {
      return this.memo;
   }

   public void setMemo(String memo) {
      this.memo = memo;
   }
}
