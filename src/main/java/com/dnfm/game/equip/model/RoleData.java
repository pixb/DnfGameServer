package com.dnfm.game.equip.model;

public class RoleData {
   int charge;
   long uid;
   String name;

   public int getCharge() {
      return this.charge;
   }

   public void setCharge(int charge) {
      this.charge = charge;
   }

   public long getUid() {
      return this.uid;
   }

   public void setUid(long uid) {
      this.uid = uid;
   }

   public String getName() {
      return this.name;
   }

   public void setName(String name) {
      this.name = name;
   }
}
